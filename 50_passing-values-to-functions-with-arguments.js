function functionWithArgs(a, b) {
  console.log(a + b)
};
functionWithArgs(1, 2);



/* Параметры — это переменные, которые действуют как заполнители для значений, которые должны быть введены в функцию при ее вызове. Когда функция определена, она обычно определяется вместе с одним или несколькими параметрами. Фактические значения, которые вводятся (или «передаются» ) в функцию при ее вызове, называются аргументами .*/